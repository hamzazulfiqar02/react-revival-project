
import { toast as reactToast } from "react-toastify"

export const SuccessToast = (message: string) => {
  return reactToast.success(message, {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  })
}

